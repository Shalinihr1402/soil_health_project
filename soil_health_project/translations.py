def translate(key: str, lang: str) -> str:
    """Look up a translation key, falling back to English then the raw key."""
    return TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS["en"].get(key, key)


_FERTILIZER_KEYS = {
    "No fertilizer required": "no_fertilizer_required",
    "Nitrogen Fertilizer": "nitrogen_fertilizer",
    "Phosphorus Fertilizer": "phosphorus_fertilizer",
    "Potassium Fertilizer": "potassium_fertilizer",
}


def translate_fertilizer(text: str, lang: str) -> str:
    """Translate a stored fertilizer recommendation string (e.g. 'Nitrogen Fertilizer, Phosphorus Fertilizer')."""
    if not text:
        return text
    parts = [part.strip() for part in text.split(",")]
    translated = [translate(_FERTILIZER_KEYS[p], lang) if p in _FERTILIZER_KEYS else p for p in parts]
    return ", ".join(translated)


